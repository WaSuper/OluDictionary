package com.dictionary.olu;

public class WordItem {

	public String word;
	public String accent;
	public String type;
	public String mean;
	
	public WordItem(String word, String accent, String type, String mean) {
		this.word = word;
		this.accent = accent;
		this.type = type;
		this.mean = mean;
	}
	
}
